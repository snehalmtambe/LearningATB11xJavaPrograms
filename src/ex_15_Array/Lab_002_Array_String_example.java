package ex_15_Array;

public class Lab_002_Array_String_example {
    public static void main(String[] args) {
        System.out.println("This is the example of String array");

        String [] str1 = new String[3];
        str1[0] = "Naman";
        str1[1] = "Vikas";
        str1[2] = "Niel";

        System.out.println("Length of String array is :"+str1.length);
        System.out.println("First element on the string array is:"+str1[0]);
        System.out.println("last element on the string array is:"+str1[2]);

    }
}
