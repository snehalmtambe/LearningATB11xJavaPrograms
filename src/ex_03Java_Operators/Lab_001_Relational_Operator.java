package ex_03Java_Operators;

public class Lab_001_Relational_Operator {
    public static void main(String[] args) {
        System.out.println("Initiating the relational operator program");
        int a = 10;
        int b = 20;

        boolean result = a>b;
        System.out.println("Result is:" +result);

        result = a < b;
        System.out.println("Result is:"+result);
    }
}
